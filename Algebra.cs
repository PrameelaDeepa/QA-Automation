using System;
using System.Collections.Generic;
using System.Text;
namespace CalcClassLibrary
{
    public class Algebra
    {
        public int Add(int a, int b)
        { return a + b; }
        public int Substract(int a, int b)
        { return a - b; }
        public int Multiply(int a, int b)
        { return a * b; }
        public float Divide(int a, int b)
        { return a / b; }
    }
}
